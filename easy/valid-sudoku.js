const isValidUnit = arr => {
  const numArr = arr.filter(el => el !== ".").sort()
  for(let i = 0; i < numArr.length - 1; i++) {
    if(numArr[i] === numArr[i + 1]) {
        return false
    }
  }
  return true
}

